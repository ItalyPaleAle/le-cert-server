// Command gen-dns-providers generates strongly-typed DNS provider credential structs
// It reads the lego provider TOML descriptors and Go sources from the pinned module version and emits:
//   - pkg/config/provider_<code>.go: one credentials struct per provider, with a newProvider method that builds the lego provider using strong types (no environment variables)
//   - pkg/config/dnsproviders_gen.go: the provider registry
//   - docs/content/dns-providers/<code>.md: one sample per provider plus _index.md
//
//nolint:forbidigo
package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"os"
	"os/exec"
	"path/filepath"
	"regexp"
	"sort"
	"strconv"
	"strings"

	toml "github.com/pelletier/go-toml/v2"
)

const generatedHeader = "// Code generated by gen-dns-providers; DO NOT EDIT."

// legoModule is the lego module path whose providers are wrapped
const legoModule = "github.com/go-acme/lego/v4"

// envVarRegexp matches valid environment-variable names
// Some lego TOMLs include descriptive prose as table keys, which we skip
var envVarRegexp = regexp.MustCompile(`^[A-Za-z_][A-Za-z0-9_]*$`)

// tomlConfig mirrors the parts of a lego provider TOML that we consume
type tomlConfig struct {
	Name          string
	Code          string
	URL           string
	Since         string
	Configuration struct {
		Credentials map[string]string
		Additional  map[string]string
	}
}

// mappedField is a credential field that maps onto a lego Config struct field
type mappedField struct {
	providerField
	// LegoField is the target field name on the lego Config struct
	LegoField string
	// Conv is the conversion kind: string, int, int64, bool, duration
	Conv string
}

// provider is the resolved model for a single DNS provider
type provider struct {
	Code                    string
	Name                    string
	URL                     string
	Since                   string
	TypeName                string
	ImportPath              string
	Fields                  []mappedField
	DefaultConfigReturnsErr bool
}

// needsStrconv reports whether the generated newProvider uses strconv
func (p provider) needsStrconv() bool {
	for _, f := range p.Fields {
		switch f.Conv {
		case "int", "int64", "bool", "duration":
			return true
		}
	}
	return false
}

// needsTime reports whether the generated newProvider uses the time package
func (p provider) needsTime() bool {
	for _, f := range p.Fields {
		if f.Conv == "duration" {
			return true
		}
	}
	return false
}

// needsURL reports whether the generated newProvider uses net/url
func (p provider) needsURL() bool {
	for _, f := range p.Fields {
		if f.Conv == "url" {
			return true
		}
	}
	return false
}

func main() {
	err := run()
	if err != nil {
		fmt.Fprintln(os.Stderr, "gen-dns-providers:", err)
		os.Exit(1)
	}
}

func run() error {
	legoDir, err := resolveLegoDir()
	if err != nil {
		return err
	}
	fmt.Fprintf(os.Stderr, "Reading lego providers from '%s'\n", legoDir)

	tomls, err := filepath.Glob(filepath.Join(legoDir, "providers", "dns", "*", "*.toml"))
	if err != nil {
		return fmt.Errorf("failed to glob lego TOMLs: %w", err)
	}
	if len(tomls) == 0 {
		return fmt.Errorf("no provider TOMLs found under '%s'", legoDir)
	}

	providers := make([]provider, 0, len(tomls))
	var skipped []string
	for _, path := range tomls {
		p, ok, parseErr := parseProvider(path, legoDir)
		if parseErr != nil {
			return fmt.Errorf("failed to parse '%s': %w", path, parseErr)
		}
		if !ok {
			skipped = append(skipped, filepath.Base(filepath.Dir(path)))
			continue
		}
		providers = append(providers, p)
	}

	sort.Slice(providers, func(i, j int) bool {
		return providers[i].Code < providers[j].Code
	})

	configDir := filepath.Join("pkg", "config")
	docsDir := filepath.Join("docs", "content", "dns-providers")

	err = cleanGenerated(configDir, docsDir)
	if err != nil {
		return err
	}

	for _, p := range providers {
		err = writeProviderFile(configDir, p)
		if err != nil {
			return fmt.Errorf("failed to write provider file for '%s': %w", p.Code, err)
		}
		err = writeProviderDoc(docsDir, p)
		if err != nil {
			return fmt.Errorf("failed to write provider doc for '%s': %w", p.Code, err)
		}
	}

	err = writeRegistry(configDir, providers)
	if err != nil {
		return fmt.Errorf("failed to write registry: %w", err)
	}

	err = writeDocsIndex(docsDir, providers)
	if err != nil {
		return fmt.Errorf("failed to write docs index: %w", err)
	}

	fmt.Fprintf(os.Stderr, "Generated %d DNS providers (skipped %d: %s)\n", len(providers), len(skipped), strings.Join(skipped, ", "))
	return nil
}

// resolveLegoDir returns the local module cache directory for the pinned lego version
func resolveLegoDir() (string, error) {
	cmd := exec.Command("go", "list", "-m", "-f", "{{.Dir}}", legoModule)
	out, err := cmd.Output()
	if err != nil {
		return "", fmt.Errorf("failed to resolve lego module dir: %w", err)
	}
	dir := strings.TrimSpace(string(out))
	if dir == "" {
		return "", fmt.Errorf("lego module dir is empty; run 'go mod download %s'", legoModule)
	}
	return dir, nil
}

// parseProvider reads and normalizes a single provider TOML and analyzes its lego source
// The second return value is false when the provider should be skipped (e.g. no typed constructor)
func parseProvider(path string, legoDir string) (provider, bool, error) {
	data, err := os.ReadFile(path) //nolint:gosec
	if err != nil {
		return provider{}, false, err
	}

	var cfg tomlConfig
	err = toml.Unmarshal(data, &cfg)
	if err != nil {
		return provider{}, false, err
	}
	if cfg.Code == "" {
		return provider{}, false, fmt.Errorf("missing Code in '%s'", path)
	}

	dir := filepath.Dir(path)
	analysis, err := analyzeLego(dir, legoDir)
	if err != nil {
		return provider{}, false, err
	}
	if !analysis.hasConstructor {
		// Providers without a typed constructor (e.g. manual) cannot be configured without environment variables
		return provider{}, false, nil
	}

	descs := make(map[string]string)
	credKeys := sortedKeys(cfg.Configuration.Credentials, descs)
	addKeys := sortedKeys(cfg.Configuration.Additional, descs)
	fields := normalizeProvider(credKeys, addKeys, descs)

	mapped := mapFields(fields, analysis)

	return provider{
		Code:                    cfg.Code,
		Name:                    cfg.Name,
		URL:                     cfg.URL,
		Since:                   cfg.Since,
		TypeName:                typeName(cfg.Code),
		ImportPath:              legoModule + "/providers/dns/" + filepath.Base(dir),
		Fields:                  mapped,
		DefaultConfigReturnsErr: analysis.defaultConfigReturnsErr,
	}, true, nil
}

// mapFields resolves each normalized field onto a lego Config field using two strategies:
// first the environment variable that lego assigns to a Config field, then the Go field name
// Fields that cannot be mapped to a supported type are dropped
func mapFields(fields []providerField, a legoAnalysis) []mappedField {
	mapped := make([]mappedField, 0, len(fields))

	// configFieldNames sorted for deterministic env-based matching
	cfgNames := make([]string, 0, len(a.configFields))
	for name := range a.configFields {
		cfgNames = append(cfgNames, name)
	}
	sort.Strings(cfgNames)

	for _, f := range fields {
		envSet := make(map[string]bool, 1+len(f.Aliases))
		envSet[f.EnvVar] = true
		for _, al := range f.Aliases {
			envSet[al] = true
		}

		target := ""
		// Strategy 1: a Config field that lego populates from one of this field's env vars
		for _, cf := range cfgNames {
			env := a.fieldEnv[cf]
			if env != "" && envSet[env] {
				target = cf
				break
			}
		}
		// Strategy 2: a Config field whose Go name matches the normalized field name
		if target == "" {
			_, ok := a.configFields[f.GoName]
			if ok {
				target = f.GoName
			}
		}
		// Strategy 3: a Config field that matches after stripping additional leading segments
		// This handles providers with multi-word env namespaces (e.g. GOOGLE_DOMAINS_ACCESS_TOKEN -> AccessToken)
		if target == "" {
			segs := strings.Split(f.EnvVar, "_")
			for drop := 2; drop < len(segs); drop++ {
				cand, _ := buildName(strings.Join(segs[drop:], "_"), false)
				_, ok := a.configFields[cand]
				if ok {
					target = cand
					break
				}
			}
		}
		if target == "" {
			fmt.Fprintf(os.Stderr, "  warn: %s has no lego Config field; dropping\n", f.EnvVar)
			continue
		}

		conv := convFor(a.configFields[target])
		if conv == "" {
			fmt.Fprintf(os.Stderr, "  warn: %s maps to unsupported type %q; dropping\n", f.EnvVar, a.configFields[target])
			continue
		}

		mapped = append(mapped, mappedField{providerField: f, LegoField: target, Conv: conv})
	}

	return mapped
}

// convFor returns the conversion kind for a lego Config field type, or "" if unsupported
func convFor(typ string) string {
	switch typ {
	case "string":
		return "string"
	case "int":
		return "int"
	case "int64":
		return "int64"
	case "bool":
		return "bool"
	case "time.Duration":
		return "duration"
	case "*url.URL":
		return "url"
	default:
		return ""
	}
}

// sortedKeys returns the sorted env-var keys of m and records each description in descs
// Keys that are not valid environment-variable names (e.g. descriptive prose entries in the TOML) are skipped
func sortedKeys(m map[string]string, descs map[string]string) []string {
	keys := make([]string, 0, len(m))
	for k, v := range m {
		if !envVarRegexp.MatchString(k) {
			continue
		}
		keys = append(keys, k)
		descs[k] = v
	}
	sort.Strings(keys)
	return keys
}

// typeName converts a lego provider code to a Go type name (e.g. "acme-dns" -> "AcmeDNSConfig")
func typeName(code string) string {
	parts := strings.FieldsFunc(code, func(r rune) bool {
		return r == '-' || r == '_'
	})
	var b strings.Builder
	for _, p := range parts {
		b.WriteString(goToken(p))
	}
	return b.String() + "Config"
}

// fileSlug converts a lego provider code to a filename-safe slug
func fileSlug(code string) string {
	return strings.Map(func(r rune) rune {
		switch {
		case r >= 'a' && r <= 'z', r >= 'A' && r <= 'Z', r >= '0' && r <= '9':
			return r
		default:
			return '_'
		}
	}, code)
}

// cleanGenerated removes previously generated files so stale providers don't linger
func cleanGenerated(configDir string, docsDir string) error {
	patterns := []string{
		filepath.Join(configDir, "provider_*.go"),
		filepath.Join(configDir, "dnsproviders_gen.go"),
		filepath.Join(docsDir, "*.md"),
	}
	for _, pat := range patterns {
		matches, err := filepath.Glob(pat)
		if err != nil {
			return err
		}
		for _, m := range matches {
			err = os.Remove(m)
			if err != nil {
				return err
			}
		}
	}
	return os.MkdirAll(docsDir, 0o755)
}

// writeProviderFile generates pkg/config/provider_<code>.go
func writeProviderFile(configDir string, p provider) error {
	var b bytes.Buffer

	fmt.Fprintf(&b, "%s\n\n", generatedHeader)
	b.WriteString("package config\n\n")

	// Imports
	b.WriteString("import (\n")
	b.WriteString("\t\"fmt\"\n")
	if p.needsURL() {
		b.WriteString("\t\"net/url\"\n")
	}
	if p.needsStrconv() {
		b.WriteString("\t\"strconv\"\n")
	}
	if p.needsTime() {
		b.WriteString("\t\"time\"\n")
	}
	b.WriteString("\n")
	b.WriteString("\t\"github.com/go-acme/lego/v4/challenge\"\n")
	fmt.Fprintf(&b, "\tprov %q\n", p.ImportPath)
	b.WriteString("\tyaml \"sigs.k8s.io/yaml/goyaml.v3\"\n")
	b.WriteString(")\n\n")

	// Struct definition
	fmt.Fprintf(&b, "// %s holds configuration for the %q DNS provider", p.TypeName, p.Code)
	if p.Name != "" {
		fmt.Fprintf(&b, " (%s)", p.Name)
	}
	b.WriteString("\n")
	if p.URL != "" {
		fmt.Fprintf(&b, "// See %s\n", p.URL)
	}
	fmt.Fprintf(&b, "type %s struct {\n", p.TypeName)
	for _, f := range p.Fields {
		fmt.Fprintf(&b, "\t%s string // %s: %s\n", f.GoName, f.EnvVar, oneLine(f.Desc))
	}
	b.WriteString("}\n\n")

	// newProvider method
	fmt.Fprintf(&b, "// newProvider builds the lego DNS challenge provider using strong types\n")
	fmt.Fprintf(&b, "// Credentials are passed directly to lego and never written to the process environment\n")
	fmt.Fprintf(&b, "func (c *%s) newProvider() (challenge.Provider, error) {\n", p.TypeName)
	if p.DefaultConfigReturnsErr {
		b.WriteString("\tcfg, err := prov.NewDefaultConfig()\n")
		b.WriteString("\tif err != nil {\n\t\treturn nil, err\n\t}\n")
	} else {
		b.WriteString("\tcfg := prov.NewDefaultConfig()\n")
	}
	for _, f := range p.Fields {
		writeFieldAssignment(&b, f)
	}
	b.WriteString("\treturn prov.NewDNSProviderConfig(cfg)\n")
	b.WriteString("}\n\n")

	// UnmarshalYAML method
	writeUnmarshal(&b, p)

	src, err := format.Source(b.Bytes())
	if err != nil {
		return fmt.Errorf("gofmt failed: %w\n%s", err, b.String())
	}

	name := filepath.Join(configDir, "provider_"+fileSlug(p.Code)+".go")
	return os.WriteFile(name, src, 0o644) //nolint:gosec
}

// writeFieldAssignment emits the code that copies a populated field onto the lego Config
func writeFieldAssignment(b *bytes.Buffer, f mappedField) {
	fmt.Fprintf(b, "\tif c.%s != \"\" {\n", f.GoName)
	switch f.Conv {
	case "string":
		fmt.Fprintf(b, "\t\tcfg.%s = c.%s\n", f.LegoField, f.GoName)
	case "int":
		fmt.Fprintf(b, "\t\tv, err := strconv.Atoi(c.%s)\n", f.GoName)
		fmt.Fprintf(b, "\t\tif err != nil {\n\t\t\treturn nil, fmt.Errorf(\"invalid value for \\\"%s\\\": %%w\", err)\n\t\t}\n", f.YAMLName)
		fmt.Fprintf(b, "\t\tcfg.%s = v\n", f.LegoField)
	case "int64":
		fmt.Fprintf(b, "\t\tv, err := strconv.ParseInt(c.%s, 10, 64)\n", f.GoName)
		fmt.Fprintf(b, "\t\tif err != nil {\n\t\t\treturn nil, fmt.Errorf(\"invalid value for \\\"%s\\\": %%w\", err)\n\t\t}\n", f.YAMLName)
		fmt.Fprintf(b, "\t\tcfg.%s = v\n", f.LegoField)
	case "bool":
		fmt.Fprintf(b, "\t\tv, err := strconv.ParseBool(c.%s)\n", f.GoName)
		fmt.Fprintf(b, "\t\tif err != nil {\n\t\t\treturn nil, fmt.Errorf(\"invalid value for \\\"%s\\\": %%w\", err)\n\t\t}\n", f.YAMLName)
		fmt.Fprintf(b, "\t\tcfg.%s = v\n", f.LegoField)
	case "duration":
		// lego interprets these values as a number of seconds
		fmt.Fprintf(b, "\t\tv, err := strconv.Atoi(c.%s)\n", f.GoName)
		fmt.Fprintf(b, "\t\tif err != nil {\n\t\t\treturn nil, fmt.Errorf(\"invalid value for \\\"%s\\\": %%w\", err)\n\t\t}\n", f.YAMLName)
		fmt.Fprintf(b, "\t\tcfg.%s = time.Duration(v) * time.Second\n", f.LegoField)
	case "url":
		fmt.Fprintf(b, "\t\tv, err := url.Parse(c.%s)\n", f.GoName)
		fmt.Fprintf(b, "\t\tif err != nil {\n\t\t\treturn nil, fmt.Errorf(\"invalid value for \\\"%s\\\": %%w\", err)\n\t\t}\n", f.YAMLName)
		fmt.Fprintf(b, "\t\tcfg.%s = v\n", f.LegoField)
	}
	b.WriteString("\t}\n")
}

// writeUnmarshal emits the strict UnmarshalYAML method
func writeUnmarshal(b *bytes.Buffer, p provider) {
	fmt.Fprintf(b, "// UnmarshalYAML decodes the provider credentials\n")
	fmt.Fprintf(b, "// It accepts the normalized name, the raw lego environment variable, and documented aliases; unknown keys error\n")
	fmt.Fprintf(b, "func (c *%s) UnmarshalYAML(value *yaml.Node) error {\n", p.TypeName)
	b.WriteString("\tif value.Kind != yaml.MappingNode {\n")
	fmt.Fprintf(b, "\t\treturn fmt.Errorf(\"dnsCredentials for DNS provider \\\"%s\\\" must be a map\")\n", p.Code)
	b.WriteString("\t}\n")
	b.WriteString("\tfor i := 0; i+1 < len(value.Content); i += 2 {\n")
	b.WriteString("\t\tvar key string\n")
	b.WriteString("\t\terr := value.Content[i].Decode(&key)\n")
	b.WriteString("\t\tif err != nil {\n\t\t\treturn err\n\t\t}\n")
	b.WriteString("\t\tvar val string\n")
	b.WriteString("\t\terr = value.Content[i+1].Decode(&val)\n")
	b.WriteString("\t\tif err != nil {\n\t\t\treturn err\n\t\t}\n")
	b.WriteString("\t\tswitch key {\n")

	seen := make(map[string]bool)
	for _, f := range p.Fields {
		labels := make([]string, 0, 3)
		for _, k := range f.acceptedKeys() {
			if seen[k] {
				continue
			}
			seen[k] = true
			labels = append(labels, fmt.Sprintf("%q", k))
		}
		if len(labels) == 0 {
			continue
		}
		fmt.Fprintf(b, "\t\tcase %s:\n\t\t\tc.%s = val\n", strings.Join(labels, ", "), f.GoName)
	}

	b.WriteString("\t\tdefault:\n")
	fmt.Fprintf(b, "\t\t\treturn fmt.Errorf(\"unknown credential key %%q for DNS provider \\\"%s\\\"\", key)\n", p.Code)
	b.WriteString("\t\t}\n")
	b.WriteString("\t}\n")
	b.WriteString("\treturn nil\n}\n")
}

// writeRegistry generates pkg/config/dnsproviders_gen.go
func writeRegistry(configDir string, providers []provider) error {
	var b bytes.Buffer

	fmt.Fprintf(&b, "%s\n\n", generatedHeader)
	b.WriteString("package config\n\n")
	b.WriteString("import \"github.com/go-acme/lego/v4/challenge\"\n\n")
	b.WriteString("// dnsProviderConfig is implemented by every generated per-provider credentials struct\n")
	b.WriteString("type dnsProviderConfig interface {\n")
	b.WriteString("\t// newProvider builds the lego DNS challenge provider using strong types\n")
	b.WriteString("\tnewProvider() (challenge.Provider, error)\n")
	b.WriteString("}\n\n")
	b.WriteString("// newDNSProviderConfig returns an empty credentials struct for the given lego provider code\n")
	b.WriteString("// The second return value is false when the code is not a supported lego DNS provider\n")
	b.WriteString("func newDNSProviderConfig(code string) (dnsProviderConfig, bool) {\n")
	b.WriteString("\tswitch code {\n")
	for _, p := range providers {
		fmt.Fprintf(&b, "\tcase %q:\n\t\treturn &%s{}, true\n", p.Code, p.TypeName)
	}
	b.WriteString("\tdefault:\n\t\treturn nil, false\n\t}\n}\n")

	src, err := format.Source(b.Bytes())
	if err != nil {
		return fmt.Errorf("gofmt failed: %w\n%s", err, b.String())
	}

	return os.WriteFile(filepath.Join(configDir, "dnsproviders_gen.go"), src, 0o644) //nolint:gosec
}

// writeProviderDoc generates docs/content/dns-providers/<code>.md
func writeProviderDoc(docsDir string, p provider) error {
	var b strings.Builder

	title := p.Name
	if title == "" {
		title = p.Code
	}

	fmt.Fprintf(&b, "---\ntitle: %q\nnav_title: %q\n---\n\n", title, title)
	fmt.Fprintf(&b, "<!-- Code generated by gen-dns-providers; DO NOT EDIT. -->\n\n")

	if p.URL != "" && strings.HasPrefix(p.URL, "http") {
		fmt.Fprintf(&b, "Configuration for the [%s](%s) DNS provider.\n\n", title, p.URL)
	} else {
		fmt.Fprintf(&b, "Configuration for the %s DNS provider.\n\n", title)
	}
	fmt.Fprintf(&b, "See the [lego documentation](https://go-acme.github.io/lego/dns/%s/) for full details.\n\n", p.Code)

	b.WriteString("```yaml\n")
	b.WriteString("letsEncrypt:\n")
	fmt.Fprintf(&b, "  dnsProvider: %q\n", p.Code)
	if len(p.Fields) == 0 {
		b.WriteString("  # This provider requires no credentials in the config file\n")
	} else {
		b.WriteString("  dnsCredentials:\n")
		for _, f := range p.Fields {
			fmt.Fprintf(&b, "    # %s: %s\n", f.EnvVar, oneLine(f.Desc))
			fmt.Fprintf(&b, "    %s: \"\"\n", f.YAMLName)
		}
	}
	b.WriteString("```\n\n")

	hasAlias := false
	for _, f := range p.Fields {
		if len(f.Aliases) > 0 {
			hasAlias = true
			break
		}
	}
	b.WriteString("Credentials are passed directly to lego using strong types and are never written to the process environment.\n")
	b.WriteString("You may also use the raw lego environment-variable names")
	if hasAlias {
		b.WriteString(" (and documented aliases)")
	}
	b.WriteString(" as keys instead of the normalized names.\n")

	name := filepath.Join(docsDir, p.Code+".md")
	return os.WriteFile(name, []byte(b.String()), 0o644) //nolint:gosec
}

// writeDocsIndex generates docs/content/dns-providers/_index.md
func writeDocsIndex(docsDir string, providers []provider) error {
	var b strings.Builder

	b.WriteString("---\ntitle: \"DNS Providers\"\nnav_title: \"DNS Providers\"\nweight: 30\n---\n\n")
	b.WriteString("<!-- Code generated by gen-dns-providers; DO NOT EDIT. -->\n\n")
	b.WriteString("le-cert-server supports the DNS providers from the [lego](https://go-acme.github.io/lego/dns/) library.\n")
	b.WriteString("Credentials are configured under `letsEncrypt.dnsCredentials` using the normalized keys shown on each provider's page, or the raw lego environment-variable names.\n")
	b.WriteString("They are passed to lego using strong types and are never written to the process environment.\n\n")
	b.WriteString("> **Note:** A few providers load their credentials through a third-party cloud SDK (for example AWS Route53/Lightsail, Google Cloud DNS, Azure DNS, Oracle Cloud, and OpenStack Designate).\n")
	b.WriteString("> For those, the SDK discovers credentials from its own environment variables, shared credentials files, or instance metadata; only the non-secret options are listed on the provider page.\n\n")
	b.WriteString("## Supported providers\n\n")

	for _, p := range providers {
		title := p.Name
		if title == "" {
			title = p.Code
		}
		fmt.Fprintf(&b, "- [%s](/dns-providers/%s)\n", title, p.Code)
	}

	return os.WriteFile(filepath.Join(docsDir, "_index.md"), []byte(b.String()), 0o644) //nolint:gosec
}

// oneLine collapses any whitespace runs in a description to single spaces
func oneLine(s string) string {
	return strings.Join(strings.Fields(s), " ")
}

// ---- lego source analysis ----

// legoAnalysis holds the facts extracted from a provider's Go source
type legoAnalysis struct {
	hasConstructor bool
	// defaultConfigReturnsErr is true when NewDefaultConfig returns (*Config, error) rather than *Config
	defaultConfigReturnsErr bool
	// configFields maps a lego Config struct field name to its type string
	configFields map[string]string
	// fieldEnv maps a lego Config field name to the environment variable lego reads it from
	fieldEnv map[string]string
}

// analyzeLego parses the Go sources of a lego provider package
// legoDir is the lego module root, used to follow Config type aliases into internal packages
func analyzeLego(dir string, legoDir string) (legoAnalysis, error) {
	a := legoAnalysis{
		configFields: make(map[string]string),
		fieldEnv:     make(map[string]string),
	}

	files, err := parseDir(dir)
	if err != nil {
		return a, err
	}

	// Resolve string-valued constants (env var names use the EnvXxx naming convention)
	consts := resolveStringConsts(files)
	imports := collectImports(files)

	// Collect the Config struct fields; the Config type may be an alias into an internal package
	aliasPath := ""
	aliasSel := ""
	for _, f := range files {
		for _, decl := range f.Decls {
			gd, ok := decl.(*ast.GenDecl)
			if !ok || gd.Tok != token.TYPE {
				continue
			}
			for _, spec := range gd.Specs {
				ts, ok := spec.(*ast.TypeSpec)
				if !ok || ts.Name.Name != "Config" {
					continue
				}
				st, ok := ts.Type.(*ast.StructType)
				if ok {
					addStructFields(st, a.configFields)
					continue
				}
				sel, ok := ts.Type.(*ast.SelectorExpr)
				if ok {
					pid, ok := sel.X.(*ast.Ident)
					if ok {
						aliasPath = imports[pid.Name]
						aliasSel = sel.Sel.Name
					}
				}
			}
		}
	}

	// Follow a Config alias into the referenced (internal) lego package
	if len(a.configFields) == 0 && aliasPath != "" && strings.HasPrefix(aliasPath, legoModule) {
		rel := strings.TrimPrefix(aliasPath, legoModule)
		targetFiles, terr := parseDir(filepath.Join(legoDir, filepath.FromSlash(rel)))
		if terr == nil {
			collectNamedStruct(targetFiles, aliasSel, a.configFields)
		}
	}

	for _, f := range files {
		for _, decl := range f.Decls {
			fd, ok := decl.(*ast.FuncDecl)
			if !ok {
				continue
			}
			switch fd.Name.Name {
			case "NewDNSProviderConfig":
				a.hasConstructor = true
			case "NewDefaultConfig":
				if fd.Type.Results != nil && fd.Type.Results.NumFields() > 1 {
					a.defaultConfigReturnsErr = true
				}
				collectFieldEnv(fd, consts, a.configFields, a.fieldEnv)
			case "NewDNSProvider":
				collectFieldEnv(fd, consts, a.configFields, a.fieldEnv)
			}
		}
	}

	return a, nil
}

// parseDir parses all non-test Go files in a directory
func parseDir(dir string) ([]*ast.File, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, err
	}

	fset := token.NewFileSet()
	files := make([]*ast.File, 0, len(entries))
	for _, e := range entries {
		name := e.Name()
		if e.IsDir() || !strings.HasSuffix(name, ".go") || strings.HasSuffix(name, "_test.go") {
			continue
		}
		f, perr := parser.ParseFile(fset, filepath.Join(dir, name), nil, 0)
		if perr != nil {
			return nil, perr
		}
		files = append(files, f)
	}
	return files, nil
}

// collectImports maps the local name of each import to its path
func collectImports(files []*ast.File) map[string]string {
	out := make(map[string]string)
	for _, f := range files {
		for _, imp := range f.Imports {
			path, err := strconv.Unquote(imp.Path.Value)
			if err != nil {
				continue
			}
			name := ""
			if imp.Name != nil {
				name = imp.Name.Name
			} else {
				name = path[strings.LastIndex(path, "/")+1:]
			}
			out[name] = path
		}
	}
	return out
}

// collectNamedStruct records the exported fields of the named struct type
func collectNamedStruct(files []*ast.File, structName string, out map[string]string) {
	for _, f := range files {
		for _, decl := range f.Decls {
			gd, ok := decl.(*ast.GenDecl)
			if !ok || gd.Tok != token.TYPE {
				continue
			}
			for _, spec := range gd.Specs {
				ts, ok := spec.(*ast.TypeSpec)
				if !ok || ts.Name.Name != structName {
					continue
				}
				st, ok := ts.Type.(*ast.StructType)
				if ok {
					addStructFields(st, out)
				}
			}
		}
	}
}

// addStructFields records the exported fields of a struct and their type strings
func addStructFields(st *ast.StructType, out map[string]string) {
	for _, field := range st.Fields.List {
		typ := exprTypeString(field.Type)
		for _, name := range field.Names {
			if name.IsExported() {
				out[name.Name] = typ
			}
		}
	}
}

// resolveStringConsts builds a map of constant name to its string value
// It runs a few passes so constants defined in terms of other constants resolve
func resolveStringConsts(files []*ast.File) map[string]string {
	consts := make(map[string]string)
	for range 4 {
		for _, f := range files {
			for _, decl := range f.Decls {
				gd, ok := decl.(*ast.GenDecl)
				if !ok || gd.Tok != token.CONST {
					continue
				}
				for _, spec := range gd.Specs {
					vs, ok := spec.(*ast.ValueSpec)
					if !ok || len(vs.Names) != 1 || len(vs.Values) != 1 {
						continue
					}
					v, ok := foldString(vs.Values[0], consts)
					if ok {
						consts[vs.Names[0].Name] = v
					}
				}
			}
		}
	}
	return consts
}

// foldString evaluates a constant string expression using already-known constants
func foldString(expr ast.Expr, consts map[string]string) (string, bool) {
	switch e := expr.(type) {
	case *ast.BasicLit:
		if e.Kind != token.STRING {
			return "", false
		}
		s, err := strconv.Unquote(e.Value)
		if err != nil {
			return "", false
		}
		return s, true
	case *ast.Ident:
		v, ok := consts[e.Name]
		return v, ok
	case *ast.BinaryExpr:
		if e.Op != token.ADD {
			return "", false
		}
		l, lok := foldString(e.X, consts)
		r, rok := foldString(e.Y, consts)
		if lok && rok {
			return l + r, true
		}
		return "", false
	default:
		return "", false
	}
}

// exprTypeString renders a type expression to a comparable string
func exprTypeString(expr ast.Expr) string {
	switch e := expr.(type) {
	case *ast.Ident:
		return e.Name
	case *ast.SelectorExpr:
		x, ok := e.X.(*ast.Ident)
		if ok {
			return x.Name + "." + e.Sel.Name
		}
		return e.Sel.Name
	case *ast.StarExpr:
		return "*" + exprTypeString(e.X)
	case *ast.ArrayType:
		return "[]" + exprTypeString(e.Elt)
	default:
		return ""
	}
}

// collectFieldEnv inspects a constructor for assignments of a Config field from an env constant
func collectFieldEnv(d *ast.FuncDecl, consts map[string]string, configFields map[string]string, out map[string]string) {
	record := func(field string, valueExpr ast.Expr) {
		_, ok := configFields[field]
		if !ok {
			return
		}
		if _, exists := out[field]; exists {
			return
		}
		env := firstEnvConst(valueExpr, consts)
		if env != "" {
			out[field] = env
		}
	}

	ast.Inspect(d.Body, func(n ast.Node) bool {
		switch node := n.(type) {
		case *ast.AssignStmt:
			for i, lhs := range node.Lhs {
				sel, ok := lhs.(*ast.SelectorExpr)
				if !ok || i >= len(node.Rhs) {
					continue
				}
				record(sel.Sel.Name, node.Rhs[i])
			}
		case *ast.KeyValueExpr:
			key, ok := node.Key.(*ast.Ident)
			if ok {
				record(key.Name, node.Value)
			}
		}
		return true
	})
}

// firstEnvConst returns the value of the first EnvXxx constant referenced in an expression
func firstEnvConst(expr ast.Expr, consts map[string]string) string {
	found := ""
	ast.Inspect(expr, func(n ast.Node) bool {
		if found != "" {
			return false
		}
		id, ok := n.(*ast.Ident)
		if !ok {
			return true
		}
		if !strings.HasPrefix(id.Name, "Env") {
			return true
		}
		v, ok := consts[id.Name]
		if ok && envVarRegexp.MatchString(v) {
			found = v
			return false
		}
		return true
	})
	return found
}
